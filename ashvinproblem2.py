l1 = [10,10,20,30,40,40,50,60,60,70,80,100,100,200,200,400,600,800,1000,1000]
l2 = [15,15,20,30,45,45,50,65,65,70,80,95,95,150,150,400,600,800,1200,1200]
#l1 = [1,3,3,4,4]
#l2 = [1,1,1,2,2]

if len(l1)%2 == 1:
    l1.append(0)
    l2.append(0)

l1.sort()
l2.sort()


print(l1)
print(l2)


cost = 0


for i in range(0,len(l1)-1,2):
    if l1[i:i+1] != l2[i:i+1]:
        if l1[i] != l2[i]:
            l1[i],l2[i] = l2[i],l1[i]
            cost+=min(l1[i],l2[i])
        elif l1[i+1] != l2[i+1]:
            l1[i+1],l2[i+1] = l2[i+1],l1[i+1]
            cost+=min(l1[i+1],l2[i+1])

print(cost)
